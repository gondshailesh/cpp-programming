// increment & decrement operator
#include<iostream>
using namespace std;

int main()
{
    int i = 1;

    cout<<i++<<endl; // post increment
    
    cout<<++i<<endl; // pre increment
    i--;

    cout<<i--<<endl; // post decrement

    cout<<++i<<endl;

    cout<<--i<<endl; // pre decrement
   
    return 0;
}
